package owl.core.util.actionTools;

public class GetterError extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public GetterError() {
	}

	public GetterError(String arg0) {
		super(arg0);
	}

	public GetterError(Throwable arg0) {
		super(arg0);
	}

	public GetterError(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
