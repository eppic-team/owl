package proteinstructure;

public class PdbaseInconsistencyError extends Exception {

	public PdbaseInconsistencyError() {
	}

	public PdbaseInconsistencyError(String arg0) {
		super(arg0);
	}

	public PdbaseInconsistencyError(Throwable arg0) {
		super(arg0);
	}

	public PdbaseInconsistencyError(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
